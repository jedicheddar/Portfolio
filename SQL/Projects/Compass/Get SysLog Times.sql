          WHERE   [action] LIKE CASE WHEN @action = '' THEN '%%' ELSE '%' + @action + '%' END
          AND     [uid] = CASE WHEN @user = '' THEN [uid] ELSE @user END
          AND     CONVERT(DATE,[createDate],101) = @date
          GROUP BY [action], [uid], DATEPART(HOUR,[createDate]), [duration]
          ) src
