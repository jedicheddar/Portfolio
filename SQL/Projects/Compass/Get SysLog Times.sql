    ORDER BY [duration] DESC
  END
  ELSE
  BEGIN
    SELECT  [action],
            COUNT(*) AS [timesRan],
            CONVERT(DECIMAL(18,2), MAX(CONVERT(DECIMAL, [duration])) / 1000) AS [maximum],
            CONVERT(DECIMAL(18,2), MIN(CONVERT(DECIMAL, [duration])) / 1000) AS [minimum],
            CONVERT(DECIMAL(18,2), SUM(CONVERT(DECIMAL, [duration])) / COUNT(CONVERT(DECIMAL, [duration])) / 1000) AS [seconds]
    FROM    [dbo].[syslog]
    WHERE   [uid] = CASE WHEN @user = '' THEN [uid] ELSE @user END
    GROUP BY [action]
    ORDER BY [seconds] DESC
  END
